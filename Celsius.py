#!/usr/env/ python
# Program that convert Celsius To Fahrenheit.
print("Enter temperature in Celsius:")
# Prompt user to input temperature in celsius.
Celsius =int(input())
# Formula to convert temperature from Celsius to Fahrenheit.
Fahrenheit = ( Celsius * 9 / 5) + 32
#print temperature in Fahrenheit.
print(f"The {Celsius} temperature in celsius is equal to {Fahrenheit} in Fahrenheit.")
print("Thank you!")
